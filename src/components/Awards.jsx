const Awards = () => {
  const [activeYear, setActiveYear] = useState(2024);

  const handlePrevYear = () => {
    setActiveYear((prevYear) => {
      const prevYearData = awardsData.find((data) => data.year === prevYear - 1);
      return prevYearData ? prevYearData.year : prevYear;
    });
  };

  const handleNextYear = () => {
    setActiveYear((prevYear) => {
      const nextYear = awardsData.find((data) => data.year === prevYear + 1);
      return nextYear ? nextYear.year : prevYear;
    });
  };

  const currentAwards = awardsData.find((data) => data.year === activeYear).awards;

  return (
    <>
    <TopSection
      subtitle="Победители"
      title="Global Construction Cup"
    />
    <div className="awards">
      <div className="year-navigation">
        <button className="arrow-button prev" onClick={handlePrevYear}>
          &lt;
        </button>
        <h2 className="year-title">Победители {activeYear} года</h2>
        <button className="arrow-button next" onClick={handleNextYear}>
          &gt;
        </button>
      </div>
      <div className="year-awards">
        <ul className="awards-list">
          {currentAwards.map((award, index) => (
            <li key={index} className="awards-item">
              <h3 className="award-category">{award.category}</h3>
              <p className="award-name">{award.name}</p>
              <p className="award-description">{award.description}</p>
            </li>
          ))}
        </ul>
      </div>
    </div>
    </>
  );
};

export default Awards;
